function kml_listColors(){
	global $XML_STYLES;
  $list = array();
	if(preg_match_all("'\"icon-\d+-([\d\w]+)-nodesc\"'", $XML_STYLES, $match)){
		$n = count($match[1]);
		foreach($match[1] as $i => $v)
			$list[$i] = $v;
	}
	return $list;
}
