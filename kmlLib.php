function kml_getPlaceMark($lat, $lng, $color, $name, $description, $options)
{
  // filter out illegal chars, such as &
  $name = str_replace( "&", "and",$name);
  $description = str_replace( "&", "",$description);
  $options = str_replace( "&", "",$options);

	$str = "			<Placemark>
				<name>".$name."</name>
				<description><![CDATA[".$description."]]></description>
				<styleUrl>#icon-1899-".$color."-nodesc</styleUrl>
				<ExtendedData>
					".$options."
				</ExtendedData>
				<Point>
					<coordinates>".$lng.",".$lat.",0.0</coordinates>
				</Point>
			</Placemark>";

	return $str;
}

